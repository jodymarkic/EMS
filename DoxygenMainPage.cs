/// \section intro Program Introduction
/// The EMS system will be designed to solve the problem of an ever-growing corporation. 
/// This system is meant to keep track of the employees working at various locations within a business. 
/// With one system keeping track of employee information (Like an employees’ salary, there home address, or how long they have been hired).<br /> <br />
/// This can be a very beneficial solution to both large and small companies/corporations.Specifically,
/// for this business, it provides a clean stand-alone system with no overhead that a network connected system may have. 
/// This can not only provide this solution to Omnicorp, but also to other businesses that require a system to maintain records about employees.
/// Finally, this system will also be modular and scalable to the point where it can grow with the business, or change environments with minimal changes. 
///
/// <hr>
/// \section requirements Product Characteristics and Requirements:
/// <ul>
/// <li>Console based</li>
/// <li>Written in C#</li>
/// <li>Data storage is flat-file</li>
/// <li>Three-tiered Architecture</li>
/// <li>Robust UI</li>
/// <li>Ensure no data loss</li>
/// </ul>
///
/// \section requirements Product Boundaries
/// <ul>
/// <li>Stand alone system - No communication between EMSs</li>
/// <li>No limit to number of employees in database</li>
/// <li>The system will be a single executable with no install required</li>
/// <li>The first iteration will specific to The Omnicorp Corporation, and not outsourced to other companies</li>
/// </ul>
